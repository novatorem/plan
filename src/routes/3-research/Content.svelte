		<h1 id="research">Research</h1>
		<p>Data types:</p>
		<ul>
			<li><strong>Quantitiative</strong>: numerical variables, measurements, statistics</li>
			<li>
				<strong>Qualitative</strong>: descriptions, anecdotes, rich data in context (words,
				pictures, observations)
			</li>
			<li><strong>Objective</strong>: observed facts, statistical evidence, impartial</li>
			<li><strong>Subjective</strong>: opinions, intuition, bias</li>
		</ul>
		<p>Evaluation Methods:</p>
		<ul>
			<li>
				<strong>Sample survey</strong>: obtain a representative sample of a population in order to
				understand population-wide trends and preferences
			</li>
			<li>
				<strong>Laboratory experiment</strong>: perform fine measurements of user performance in
				tightly controlled laboratory conditions
			</li>
			<li>
				<strong>Field study</strong>: understand underlying user needs in the wild by observing them
				in their environment
				<ul>
					<li><strong>Naturalistic</strong> observation: no involvement, observation only</li>
					<li><strong>Questionnaires</strong>: wide distribution, many responses</li>
					<li><strong>Interviews</strong>: great detail, adapt to responses</li>
					<li><strong>Focus groups and workshops</strong>: multiple participants, consensus</li>
					<li><strong>Studying documentation</strong>: existing process/system</li>
					<li><strong>Diaries/logging</strong>: details on duration of usage and performance</li>
				</ul>
			</li>
		</ul>

