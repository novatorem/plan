		<h1 id="introduction">Introduction</h1>
		<p>Let's begin with some definitions to better understand what needs to be accomplished.</p>
		<h2 id="human-computer-interaction">Human-Computer Interaction</h2>
		<p>
			An interdisciplinary field focused on the <strong>interactions</strong> between human users
			and computer systems, including the user interface that seeks to create
			<strong>effective, efficient, and satisfying interactions.</strong>
		</p>
		<h2 id="interaction-design">Interaction Design</h2>
		<ul>
			<li>Designing interactive products to support people in their everyday and working lives</li>
			<li>Design of spaces for human communication & interaction</li>
		</ul>
		<h2 id="user-experience">User Experience</h2>
		<ul>
			<li>Everything that the user sees, hears and touches</li>
			<li>The overall experience a user has with a product, service or event</li>
			<li>How satisfying, usable, and well-designed users perceive an interface to be</li>
		</ul>
		<p>
			This is the phenomenon you should understand and strive to improve in order to succeed when
			designing interactions.
		</p>
		<h2 id="iteration-and-evaluation">Iteration and Evaluation</h2>
		<p>The user experience is a crucial driving force in interaction design.</p>
		<p>
			User-Centered Design (UCD) involves testing with users at every stage of the design process.
		</p>
		<ul>
			<li>Needs and requirements</li>
			<li>Develop designs</li>
			<li>Build prototypes</li>
			<li>Evaluate designs</li>
		</ul>

